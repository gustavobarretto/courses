// EXERCÍCIO: Meu primeiro if
let status = true
if (status ==  true) {
    console.log(true)
    console.log('O servidor está on-line.')
} else if (status == false) {
    console.log(false)
    console.log('O servidor está offline')
} else {
    console.log('Algo está errado. Não se consegue avaliar as condições do servidor.')
}